INT32 stdgbkfindhz(UINT16 in_shIn)
{
  UINT32 p, h, t;

  h = 0;
  t = 21920;
  p = 21920 / 2;
  while(p <= t && p >= h && t != h)
  {
    if(in_shIn == _GB2312S[p])
    {
      return 1;
    }
    else if(t - h < 4)
    {
      for(p = h; p <= t; p++)
      {
        if(in_shIn == _GB2312S[p])
          return 1;
      }
      return 0;
    }
    else if(in_shIn > _GB2312S[p])
    {
      h = p;
      p = p + (t - p) / 2;
    }
    else
    {
      t = p;
      p = h + (p - h) / 2;
    }
  }
  return 0;
}

