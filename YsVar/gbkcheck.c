INT32 FEGbkfilter(char *TS,char *SS)
{
  INT32 iLen1;
  UINT16 iHZ;

  iLen1=0;
  while(*SS) {
    /*  */
    if((UCHAR)(*SS)==(UCHAR)0xff || 
      (UCHAR)*SS<(UCHAR)0x81) {

      if((*SS==(char)9) || (*SS==(char)10) ||
         (*SS==(char)13) || 
         (*SS>=(char)32 && *SS<=(char)126)) {
        *(TS+iLen1)=*SS;
        iLen1++;
      }
      SS++;
      continue;
    } else {
      /* GBK  |  */
      if((UCHAR)*(SS+1)<0x81 || 
        (UCHAR)(*(SS+1))==0xff) {
        SS++;
        continue;
      }
      iHZ=(UCHAR)(*SS)*256+((UCHAR)(*(SS+1)));
      /*  */
      if(stdgbkfindhz((unsigned short)iHZ)) {
        *(TS+iLen1)=*SS;
        *(TS+iLen1+1)=*(SS+1);
        iLen1+=2;
        SS=SS+2;
        continue;
      } else {
        SS++;
        SS++;
        continue;
      }
    }
  }

  *(TS+iLen1)=0;
  return 0;
}

